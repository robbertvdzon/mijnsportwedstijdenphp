<b>Wat kan ik als competitiebeheerder bijdragen?</b><br>
Als competitiebeheerder is het mogelijk om het competitie programma en eventueel ook de wedstrijd uitslagen te  delen op www.mijnsportwedstrijden.nl.
<br><br>
Teamspelers kunnen dan hun competitie zoeken, hun team selecteren en zich inschrijven op dat team.<br>
Ze hebben dan direct alle wedstrijden in het overzicht.<br> 
Als de competitie niet gepubliceerd is moeten alle wedstrijden handmatig ingevoerd worden.<br>
Als een competitie dus gedeeld is kunnen fouten voorkomen worden en bovendien een stuk eenvoudiger voor de teams.<br>
